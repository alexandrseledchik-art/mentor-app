import type { DiagnosticInput, DiagnosticStructuredResult } from "./schema";

function getLeadArea(input: DiagnosticInput) {
  const text = input.userMessage.toLowerCase();

  if (text.includes("продаж") || text.includes("лид") || text.includes("клиент")) {
    return {
      contour: "commercial" as const,
      area: "коммерция и продажи",
      situation: "просадка коммерческого контура",
    };
  }

  if (text.includes("касс") || text.includes("деньг") || text.includes("прибыл")) {
    return {
      contour: "finance" as const,
      area: "финансы и управленческий учёт",
      situation: "непрозрачность финансового контура",
    };
  }

  if (text.includes("команд") || text.includes("роль") || text.includes("ответствен")) {
    return {
      contour: "team" as const,
      area: "команда и ответственность",
      situation: "people-dependency",
    };
  }

  if (text.includes("хаос") || text.includes("процесс") || text.includes("ручн")) {
    return {
      contour: "operations" as const,
      area: "операции и управляемость",
      situation: "операционный хаос",
    };
  }

  return {
    contour: "management" as const,
    area: "управление и приоритизация",
    situation: "размытая управляемость",
  };
}

